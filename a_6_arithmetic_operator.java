import java.lang.*;
public class a_6_arithmetic_operator {
    public static void main(String[] args) {
        int a=12,b=24;
        System.out.println("a\t"+a+"\tb\t"+b);
        System.out.println("a+b\t"+(a+b));
        System.out.println("a-b\t"+(a-b));
        System.out.println("a*b\t"+(a*b));
        System.out.println("b/a\t"+(b/a));
        System.out.println("a%b\t"+(a%b));
    }
}
