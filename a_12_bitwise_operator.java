public class a_12_bitwise_operator {
    public static void main(String[] args) {
        int a=2,b=3;
        System.out.println("a\t"+a+"\tb\t"+b);
        System.out.println("Bitwise and (a&b)\t"+(a&b));
        System.out.println("Bitwise or (a|b)\t"+(a|b));
        System.out.println("Bitwise xor (a^b)\t"+(a^b));
        System.out.println("Bitwise complement (~a)\t"+(~a));
        System.out.println("Bitwise left shift (a<<3)\t"+(a<<3));
        System.out.println("Bitwise right shift (a>>1)\t"+(a>>1));


    }
}
