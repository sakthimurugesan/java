public class a_11_unary_operator
{
    public static void main(String[] args)
    {
        int a=12;
        System.out.println("a\t"+a);
        System.out.println("a++\t"+a++);
        System.out.println("a\t"+a);
        System.out.println("a--\t"+a--);
        System.out.println("a\t"+a);
        a=12;
        System.out.println("a\t"+a);
        System.out.println("++a\t"+(++a));
        System.out.println("a\t"+a);
        System.out.println("--a\t"+(--a));
        System.out.println("a\t"+a);

    }
}
