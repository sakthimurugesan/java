import java.lang.*;
public class a_7_assignment_operator
{

    public static void main(String[] args)
    {
        int a=12;
        System.out.println("a\t"+a);
        System.out.println("a+=5\t"+(a+=5));
        System.out.println("a-=5\t"+(a-=1));
        System.out.println("a*=2\t"+(a*=2));
        System.out.println("a/=2\t"+(a/=2));
        System.out.println("a%=3\t"+(a%=3));
        System.out.println("a<<=3\t"+(a<<=3));
        System.out.println("a>>=2\t"+(a>>=2));


    }
}
