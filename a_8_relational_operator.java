public class a_8_relational_operator
{
    public static void main(String[] args)
    {
        int a=12,b=5;
        System.out.println("a\t"+a+"\tb\t"+b);
        System.out.println("a>b\t"+(a>b));
        System.out.println("a>=b\t"+(a>=b));
        System.out.println("a<b\t"+(a<b));
        System.out.println("a<=b\t"+(a<=b));
        System.out.println("a==b\t"+(a==b));
        System.out.println("a!=b\t"+(a!=b));
    }
}
